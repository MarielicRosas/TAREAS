#Elabora un programa que lea un arreglo de 10 posiciones que contenga las edades de 10 de sus compañeros,
#calcule el promedio de las edades, obtenga la edad mayor y la edad menor. Al final imprima todos los elementos de la lista

edades = []
#Va a leer las edades
for i in range(10):
  edad = int(input(f"Introdusca la edad {i + 1}: "))
  edades.append(edad)

#Va a sacar el promedio de las edades
sumaedades = sum(edades)
promedio = sumaedades / 10

#Indicar la edad mayor y menor
edadmayor = max(edades)
edadmenor = min(edades)

# Imprimir los resultados
print("\nResultados:")
print(f"El promedio de las edades es: {promedio:.2f}")
print(f"La edad mayor es: {edadmayor}")
print(f"La edad menor es: {edadmenor}")
print("Las siguientes son todas las edades:")
for i, edad in enumerate(edades, start=1):
  print(f"El compañero numero {i} tiene {edad} años")

